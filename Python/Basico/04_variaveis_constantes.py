
#   Variáveis 
#   -> Armazena Valores.
#   -> Pode ser alterado no decorrer do código.
#   Ex.:

age = 21 # variavel
print(f'Thiago tem {age} anos')
age = 28 # variavel
print(f'Thiago tem {age} anos')


#   Constantes
#   -> Armazena Valores.
#   -> Não pode ser alterado no decorrer do código.
#   Obs: Python não tem constante, mas por convenção utilizamos uma varivavel com todas as letras em maiusculo para chamar de 'constante'.

NOME = "Thiago"   # 'constante'.
print(NOME)

NOME = 'teste'  # Como vemos aqui ela pode ser alterada, pois python não possui constante.
print(NOME, age)



