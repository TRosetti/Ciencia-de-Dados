'''
    Operadores Aritméticos

Os operadores aritiméticos executam operações matemáticas, como adição, subtração com operadores.

'''

# Adição 
print(1 + 1)        #   _> 2

# Subtração 
print(10 -2)        #   _> 8

# Multipicação     
print(4 * 3)        #   _> 12

# Divisão
print(12 / 3)       #   _> 4.0

# Divisão Inteira
print(12 // 3)      #   _> 4

# Módulo / Resto da Divisão
print(10 % 3)       #   _> 1

# Exponenciação
print(2 ** 3)       #   _> 8


'''
    Ordem de Prioridade 
    -> Parêntesis
    -> Expoêntes
    -> Multiplicações e divisões (da esquerda para direita)
    -> Soma e subtração (da esquerda para direita)
'''
print(10 - 5 * 2)       #   _> 0

print((10 - 5) * 10)    #   _> 50

print(10 ** 2 * 2)      #   _> 200

print(10 ** (2 * 2))    #   _> 10000

print(10 / 2 * 4)       #   _> 20.0