'''
    Operadores de Comparação

Utilizados para comparar dois valores.

'''
saldo = 450
saque = 200

#   Igualdade   ( == ) 
print( saldo == saque )     #   _> False

#   Diferença   ( != )
print(saldo != saque)       #   _> True

#   Maior que   ( > )
print(saldo > saque)        #   _> True

#   Maior ou Igual  ( >= )  
print(saldo >= saque)       #   _> True

#   Menor que   ( < )
print(saldo < saque)        #   _> False

#   Menor ou Igual  ( >= )  
print(saldo <= saque)       #   _> False