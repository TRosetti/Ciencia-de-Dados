# Tipos em Python

#  |  Texto         |   str                             |
#  |  Númerico      |   int, float , complex            |
#  |  Sequência     |   list, tuple, range              |
#  |  Mapa          |   dict                            |
#  |  Coleção       |   set, fronzenset                 |
#  |  Booleano      |   bool                            |
#  |  Binário       |   bytes, bytearray, memoryview    |



#   Números Inteiros (int) -> 1, 10, 500542524524, -2351, -2 ,...

#   Números de Ponto Flutuante (float)  -> números racionais, ex.: 1.5, -10.543, 0.76... 

#   Booleano -> usado para representar verdadeiro ou falso (True or False), qualquer numero ≠ 0 representa verdadeiro e 0 representa falso

#   String -> valores alfanúmerico, ex.: "Python", 'Python', """Python""", ""Python"", "p"


print(1 + 10) # int

print(1 + 1.5 + 100) # float

print(True) # bool

print("Thiago") # str

