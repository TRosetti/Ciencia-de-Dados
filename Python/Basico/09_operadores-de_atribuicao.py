'''
    Operadores de Atribuição

São operadores utilizado para definir o valor inicial ou sobrescrever o valor de uma variável.

'''

#   Atribuição Simples      ( = )
saldo = 500     # saldo 'recebe' 500
print(saldo)    #   _> 500


#   Atribuição com Adição   ( += )
saldo = 500
saldo += 200    #   saldo = saldo + 200
print(saldo)    #   _> 700


#   Atribuição com subtração  ( -= )
saldo = 500
saldo -= 200    #   saldo = saldo - 200
print(saldo)    #   _> 300


#   Atribuição com Multiplicação   ( *= )
saldo = 500
saldo *= 2      #   saldo = saldo * 2
print(saldo)    #   _> 1000


#   Atribuição com Divisão   ( /= )
saldo = 500
saldo /= 2      #   saldo = saldo / 2
print(saldo)    #   _> 250.0


#   Atribuição com Divisão Int  ( //= )
saldo = 500
saldo //= 2     #   saldo = saldo // 2
print(saldo)    #   _> 250


#   Atribuição com Módulo   ( %= )
saldo = 500
saldo %= 2      #   saldo = saldo % 2
print(saldo)    #   _> 0


#   Atribuição com Exponenciação   ( **= )
saldo = 500
saldo **= 2     #   saldo = saldo ** 2
print(saldo)    #   _> 250000
